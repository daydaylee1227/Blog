const letterCasePermutation = S => {
    const ans = []
    const backtrack = (str, i) => {
      if (i ===  S.length) {
         return ans.push(str)
      }
  
      const curr = S[i]
      if ((curr >= 'A' && curr <= 'Z') || (curr >= 'a' && curr <= 'z')) {  // å¤§å°å†™å­—æ¯æœç´¢ä¸¤æ¬¡
          const   low = str + curr.toLowerCase(),
                  high = str + curr.toUpperCase()
          backtrack(low, i + 1)
          backtrack(high, i + 1)
      } else {  // æ•°å­—çš„è¯,ç›´æŽ¥ç»§ç»­é€’å½’
        backtrack(str + curr, i + 1)
      }
    }
    backtrack("", 0)
    return ans
  }
  